

/* 
Таблица умножения
*/

public class Solution {
    public static void main(String[] args) {
        //напишите тут ваш код
        for (int i = 1; i < 11; i ++)
        {
                for (int j = 1; j < 11; j ++)
                {
                
                       System.out.print(j * i + " ");
                
                }
                System.out.println(" ");
               
        }
      
  
    }
    
}
